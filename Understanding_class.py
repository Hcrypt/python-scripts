class Person:
  def __init__(self, name, age, hobby):
    self.name = name
    self.age = age
    self.hobby = hobby

p1 = Person("John", 18, "Programming")
p2 = Person("Bob", 22, "Graphic Designing")

print ("")
print(p1.name)
print(p1.age)
print(p1.hobby)

print("")
print(p2.name)
print(p2.age)
print(p2.hobby)
